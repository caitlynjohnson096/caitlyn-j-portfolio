        <h2> Key Takeaways </h2>

          <p> After finishing this project some of the key takeaways are learning to implement interactivity that improves the user experience. In this project, the reviews section that had interactivity added to it as a view more/less button was not well-designed as it did not improve the user experience. The goal of this interactivity was to decrease the amount of scrolling that a user had to do. This is because the reviews took up a lot of space on the page and were quite repetitive; however, by shorening each indivual review the overall going of deceasing respitivness, and the amount of space that these reviews took up was lost. Therefore, for future project it is important to keep in mind when using javascript that the design of the   page we are implementing musst benefit the user in some way. </p>
