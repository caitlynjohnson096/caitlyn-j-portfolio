        <p>Another change that will be implemented is ensuring that the site is overall accessible especially when it comes to interactivity. After watching users complete the user-tests, it became apparent that we were missing cursors or changes in buttons to show the user that they can click there. While, working on this design we will add this signifiers to indicate to users where they can click as well as aria-labels for a screen-reader.</p>

        <p>We also decided to rename the "Autobody repair" navigation tab because users were having trouble finding the certifications. We decided to name it "Autobody work & certifications". This way the user would get a better sense for what was on the page.</p>

        <p> We also decided to change the about us page to include an image that gives more information about what clarks is about. After our first user test, it was determined that some people had trouble identifying how clark is involved in the community, so we decided to add an image from Avya's ride on the site. This also caused the site to need to be redesigned especially in the desktop version to ensure that the image fit on the page well. See the redesign below </p>

