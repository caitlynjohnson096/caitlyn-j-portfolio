        <p> Once we implemented our skteches, we then began the user testing process where we would gauge the ease of use and accessibility of our site by testing it on potential users. </p>

        <p> When the user testing was completed we decided to implement several changes to the design based on feedback from our users</P>

        <p>As a result of the user testing the "Autobody Repair" page will need to alter the background of the site when the certifications are clicked, so that the user can notice the changes in the site (the "pop-up") more easily. Look Below to see the redesign </p>

