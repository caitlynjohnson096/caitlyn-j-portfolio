<h2> Problem </h2>
<p>How can we motivate  and educate urban dwellers on gardening so that they are able to interact with their physical space thus promoting a healthier lifestyle? </p>

<h2> Solution </h2>
<p>
A gardening app that aims to motivate  and provide knowledge to urban dwellers who are attempting to garden . The app utilizes daily to-dos, check-ins, plant growing in a virtual space, and personalized plant kits to help bridge knowledge gaps and motivate the user to grow a plant successfully.</p>

