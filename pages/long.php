<h3> Survey Findings </h3>

<p>From the survey we were able to conclude that 94% of our participants had a high interest in attempting to garden or had gardened in the past. Additionally, we were able to conclude that 39.9% of participants ranked time as one of their top pain points with space and lack of knowledge making up 23% and 16.9% of participants respectively. </p>

